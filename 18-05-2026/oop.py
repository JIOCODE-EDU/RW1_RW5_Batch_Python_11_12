# Python Objects & Classes

# 1. Encapsulation

# Warpping data (variables) and methods(functions) together inside a single class.

# It also hepls in data hiding and security.

# Real-Time Examples

# Think about ATM Machine:

# you can use ATM functions. but internal banking details are hidden.

# Advantage

# Data security , Prevent direact access , Better control over data

# 2. Inheritance

# one class can use properties and methods of another class.

# Parent class features can be reused in child class.

# Real-Time Examples

# Father ---> Son

# Surname
# Properties
# features

# Advantage

# Code Reusability , Less code duplication , Easy maintance , Type of inheritance , type meaning

# 3. Polymerphism

# Same method name behaves diffrently.

# Real-Time Examples

# Father at Home
# Employee at office
# Friend outside

# Advantage

# Flexibility , Reusable methods , Cleaner code , diffrent Arguments

# 4. Abstraction

# Hiding internal implementation and showing only important features.

# Advantage : Hides Complexity , Better security , Cleaner Code , Code Resuability









